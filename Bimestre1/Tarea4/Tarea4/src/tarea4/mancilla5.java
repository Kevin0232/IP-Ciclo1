public class mancilla5 {
    public static void main(String[] args) {
        System.out.println("Al usar un contador i cuenta que tiene valor "
                + "inicial de 20, un valor final" +
                "de 1 y un incremento de -2.");
        //Declaración e inicialización de variables
        int icuenta, n;
        icuenta=20; n=1;
        while(icuenta>=n){
            
            System.out.println(icuenta);
            icuenta=icuenta-2;
        }
    }
}
