public class mancilla2 {
    public static void main(String[] args) {
        System.out.println("Usar un contador icuenta que tiene un valor inicial de 1, un valor final" +
                "20 y un incremento desar un cont 2.");
        //Declaración e inicialización de variables
        int icuenta, n;
        icuenta=1; n=20;
        while(icuenta<=n){
            if (icuenta==1) {
                icuenta=icuenta + 1;
            }
            System.out.println(icuenta);
            icuenta=icuenta+2;
            
        }
    }
}
